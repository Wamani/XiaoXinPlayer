package com.xiaoxintech.xiaoxinplayer.MusicService;

public class Song {

    private String path;
    private String play_path="";

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getPlayPath() {
        return play_path;
    }

    public void setPlayPath(String path) {
        this.play_path = path;
    }
}